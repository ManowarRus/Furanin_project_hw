import pytest

from src.processing import list_dictionary, list_sorted_date


def test_list_dictionary(list_dict):
    assert list_dictionary(list_dict) == [
        {"date": "2019-07-03T18:35:29.512364", "id": 41428829, "state": "EXECUTED"},
        {"date": "2018-06-30T02:08:58.425572", "id": 939719570, "state": "EXECUTED"},
    ]


def test_list_dictionary_none() -> None:
    assert list_dictionary([]) == []


